var x = 10;
var y = 20;
// AND Operator
var resultAND = (x > 15) && (y > 15); // false AND true = false
//console.log(resultAND);
// OR logical Operator
var resultOR = (x > 15) || (y > 15); //false OR true = true 
//console.log(resultOR);
//NOT Operator 
var resultNOT = (!resultOR);
console.log(resultNOT);
